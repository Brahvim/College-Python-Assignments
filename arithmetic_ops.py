# Python has the five fundamental arithmetic operators every other language does:

print(5 + 5)
print(2 - 4)
print(6 * 7)
print(2 / 10)
print(5 % 10)

# There are also two additional operators for "floor division" and exponentiation:
print(2**5)
print(5 // 2.5)

# Python supports shorthands for all of them:
a = 5
a += 2
a -= 1
a *= 10
a /= 10
a **= 10

